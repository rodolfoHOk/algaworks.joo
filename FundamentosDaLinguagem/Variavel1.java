public class Variavel1 {

	public static void main(String[] args) {
	
		int quantidade; // declarando variável de número inteiro
		quantidade = 10; // atribuindo o valor 10
		
		System.out.println(quantidade);
		
		quantidade = 15; // atribuindo o valor 15
		
		System.out.println(quantidade);
		
		// Nomeando variáveis 
		
		// permitidas
		int quantidade_alunos;
		int QUANTIDADE;
		int QuantidadeAlunos;
		int $quantidade;
		int _quantidade;
		int quantidade_alunos_nota_10;
		
		// não permitidas
		// int 2alunos; // não pode começar com digitos
		// int quantidade alunos; // não pode ter espaços
		// int new; // não pode ser palavras reservadas
		
		// seguindo as convenções
		int quantidadeAlunos;
		int quantidadeAlunosNota10;
		int numeroDeAlunosAprovados;
		
		// não é boa prática
		int qtAlu; // abreviação excessiva
		int quantAlunN10; // abreviação excessiva
	
	}
	
}
