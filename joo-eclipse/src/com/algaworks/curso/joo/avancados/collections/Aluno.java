package com.algaworks.curso.joo.avancados.collections;

public class Aluno {

	private String nome;
	private int idade;
	
	public Aluno(String nome) {
		super();
		this.nome = nome;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}

	public String getNome() {
		return nome;
	}
	
}
